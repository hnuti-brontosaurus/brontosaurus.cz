		<h2>
			Průběh akce
		</h2>

		<ul class="list hb-mbe-4">
			<li class="list__item">
				Na místo konání se dopravuje většinou každý zvlášť v pátek večer, pak následují hry k seznámení s ostatními.
				Nejčastěji jezdí středoškoláci, vysokoškoláci, ale i mladí pracující.
			</li>

			<li class="list__item">
				Dobrovolnické práci se věnujeme v sobotu v délce 5 až 8 hodin. Spočívá třeba v kosení luk,
				opravování hradů, sázení stromů, vyvěšování ptačích budek a podobně.
			</li>

			<li class="list__item">
				O víkendu se budeme věnovat aktivitám pro rozvoj osobnosti, které zároveň stmelují kolektiv.
				Čekají tě akční pecky, propracované psychohry i rozjímání, záleží navpovaze akce.
			</li>

			<li class="list__item">
				A nechybí ani vzdělávání – přednášky o lokalitách, prohlídky, exkurze nebo workshopy.
			</li>

			<li class="list__item">
				Nejčastěji odjíždíme kolem poledne, aby se všichni dostali včas domů.
			</li>
		</ul>

